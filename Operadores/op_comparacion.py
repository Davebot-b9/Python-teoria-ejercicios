#Los operadores de comparacion devulven o True o False, que se utilizan mucho con condicionales

es_igual_que = 5 == 6
print(es_igual_que)

es_distinto_de = 5 != 6
print(es_distinto_de)

mayor_que = 5 > 6
print(mayor_que)

menor_que = 5 < 6
print(menor_que)

mayor_o_igual = 5 >= 6
print(mayor_o_igual)

menor_o_igual = 5 <= 6
print(menor_o_igual)

